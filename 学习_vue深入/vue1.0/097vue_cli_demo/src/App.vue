<template>
  <div id="app">
    <img src="./assets/logo.png">
    <h1 track-by="$index">{{ msg }}</h1>
    <input type="button" value="点击改变" @click="change" />
  </div>
</template>

<script>
export default {
  name: 'app',
  data () {
    return {
      msg: 'Welcome to Your Vue.js App'
    }
  },
  methods:{
  	change(){
  		this.msg=this.msg.split('').reverse().join('');
  	}
  }
}
</script>

